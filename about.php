        <h2>About Us</h2>
        Our Quiz App Project aims to provide an interactive and engaging platform for users to test their knowledge and
        have fun while learning. We have designed this app to cater to a wide range of users, including students, trivia
        enthusiasts, and anyone interested in challenging their knowledge on various topics.
        <br><br>
        Key Features:
<br><br>
       1. User-Friendly Interface: Our app offers a simple and intuitive user interface, ensuring a seamless experience
        for all users. Navigating through different sections and accessing quizzes is made easy and convenient.
<br><br>
       2. Multiple Quiz Formats: We offer various quiz formats to keep the experience engaging and exciting. Users can
        enjoy multiple-choice questions, true or false statements, fill in the blanks, and more. This variety adds
        dynamism to the quizzes and allows users to explore different question styles.
<br><br>
        3.Leaderboards and Competitions: To add a competitive element, our app features leaderboards where users can see
        their rankings based on quiz scores. Users can also participate in timed quizzes and compete with friends or
        other app users, fostering a sense of community and friendly competition.
<br><br>
       4. User Feedback and Support: We value user feedback and have incorporated a robust feedback system within the app.
        Users can provide suggestions, report issues, and request new quiz categories. Additionally, our dedicated
        support team is readily available to address any user concerns or inquiries.

